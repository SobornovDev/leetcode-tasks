package solutions.palindrome_linked_list;

/**
 * @author : Sobornov Vladimir
 * @since : 06.11.2022
 **/
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
